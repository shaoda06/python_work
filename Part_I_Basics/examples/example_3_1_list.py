# how to indicate a list
fruit = ['apple', 'peach', 'banana', 'pear']
print(fruit)

# Accessing Elements in a List
print(fruit[0])
# apple

# Index Positions Start at 0, Not 1
number1 = 1
number2 = 2
num_list = [number1, number2]
print(num_list)
# index [0, 1]
print(num_list[1])
# 2


# To return the elements in the end of a list
# -1 - the last - the 1st from the end of the list
# -2 the 2nd from the end of the list
# -3 the 3rd from the end of the list
# and so forth
print(fruit[-1])
# pear
print(fruit[-2])
# banana
print(fruit[-3])
# peach
