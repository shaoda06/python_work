############### Organizing a List ###############

# Sorting a List Permanently with the sort() Method
cars_1 = ['bmw', 'audi', 'toyota', 'subaru']
cars_1.sort()
print(cars_1)
cars_1.sort(reverse=True)
print(cars_1)

# Sorting a List Temporarily with the sorted() Function
cars_2 = ['bmw', 'audi', 'toyota', 'subaru']
print("Here is the sorted list:\n" + str(sorted(cars_2)))
print("Here is the original list:\n" + str(cars_2))

# Printing a List in Reverse Order
cars_3 = ['bmw', 'audi', 'toyota', 'subaru']
print(cars_3)
cars_3.reverse()
print(cars_3)

# Finding the Length of a List
cars_4 = ['bmw', 'audi', 'toyota', 'subaru']
print(len(cars_4))




