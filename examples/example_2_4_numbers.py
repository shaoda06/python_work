# 2.4.1 Integers
# You can add (+), subtract (-), multiply (*), and divide (/) integers in Python.
print(2 + 3)
print(3 - 2)
print(2 * 3)
print(3 / 2)
# Python uses two multiplication symbols to represent exponents:
print(3 ** 2)
print(3 ** 3)
print(10 ** 6)
# order of operations
print(2 + 3 * 4)
print((2 + 3) * 4)

# 2.4.2 Floats
# Python calls any number with a decimal point a float.
print(0.1 + 0.1)
print(0.2 + 0.2)
print(2 * 0.1)
print(2 * 0.2)

